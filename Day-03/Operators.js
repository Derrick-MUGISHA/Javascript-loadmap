// operators are symbols that perform operations on operands (values and variables ) to produce a result.

// 1 Mathematical Operators 
// addition: +
// subtraction: -
// multiplication: *
// division: /
// modulus: %
// exponent: **

//  a addition : +

let a = 2;
let b = 5;

let sum = a + b;
console.log("The sum of a and b is: " + sum);

// b subtraction : -

let sub = b - a;
console.log("The subtraction of a and b is: " + sub);

// c multiplication : *

let mul = a * b;
console.log("The multiplication of a and b is: " + mul);

// d division : /

let div = b / a;
console.log("The division of a and b is: " + div);

// e modulus : %

let mod = b % a;
console.log("The modulus of a and b is: " + mod);


// f exponent : **

let exp = a ** b;
console.log("The exponent of a and b is: " + exp);